/*
 * SLIDE 9 - MOD5 - REPETICAO FOR
 * Faça um programa para mostrar os números de 1 a 10
 * em ordem invertida. Use a estrutura de repetição: for
 */
package repeticaoforinvertido;

public class RepeticaoForInvertido {

    public static void main(String[] args) {
// int i=10 => cria e inicializa a variável i
// i>0 => condição
// i-- => controle do loop (contador) => i=i-1;

        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }
        System.out.println("Fim...");
    }

}
